package kamilacode.logicadeprogramacao;

public class Variaveis {

    public static  void main(String[] args) {
        byte variavelByte = 127;
        System.out.println(variavelByte);


        short variavelShort = 32767;
        System.out.println(variavelShort);

        int variavelInt = 2147483647;
        System.out.println(variavelInt);

        long variavelLong = 10000000L;
        System.out.println(variavelLong);

        float variavelFloat = 123.4F;
        System.out.println(variavelFloat);

        double variavelDouble = 1234567890.1;
        System.out.println(variavelDouble);

        char variavelChar = 'a';
        System.out.println(variavelChar);

        boolean javaELegal = true;

        System.out.println(javaELegal);



    }
}
